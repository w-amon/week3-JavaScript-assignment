// Initialize a variable 'count' to 0. This variable will be used to keep track of the count.
let count = 0;

// Start a 'for' loop. The loop will run as long as the 'count' is less than or equal to 20.
for( ; count <= 20; ){

    // Inside the loop, print the current value of 'count' to the console.
    console.log(count);

    // Increment the value of 'count' by 1 for the next iteration.
    count++;
}